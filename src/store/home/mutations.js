import * as types from './mutation-types'

export const mutations = {
  [types.GETNEWS] (state, o) {
    // console.log("GETNEWS",o)
    state.newsData = o
  },
  
  [types.PRODUCTDATA] (state, o) {
    // console.log("GETNEWS",o)
    state.productData = o
  },
  [types.HOMEPAGEDATA] (state, o) {
    // console.log("GETNEWS",o)
    state.homePageData = o
  }
}
